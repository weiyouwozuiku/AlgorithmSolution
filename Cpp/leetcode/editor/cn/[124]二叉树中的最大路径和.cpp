 // æœ¬é¢˜çš„è§£é¢˜æ€è·¯æ˜¯ï¼š
 // dp+dfsã€‚æœ¬é¢˜å¯ä»¥æ‹†è§£æˆæœ€å¤§è·¯å¾„å°±æ˜¯ans=max(ans,root->val+left+right)
 // éœ€è¦æ³¨æ„çš„æ˜¯è¿”å›å€¼ä¸€å…±æœ‰ä¸‰ç§å¯èƒ½ï¼Œåªèµ°åˆ°rootæˆ–è€…left+rootæˆ–è€…right+root å› æ­¤è¿”å›root->val + max(left, right)
class Solution {
public:
    int ans;

    int maxPathSum(TreeNode *root) {
        ans = INT_MIN;
        dfs(root);
        return ans;
    }

    int dfs(TreeNode *root) {
        if (!root) return 0;
        int left = 0, right = 0;
        if (root->left) left = max(0, dfs(root->left));
        if (root->right) right = max(0, dfs(root->right));
        ans = max(ans, root->val + left + right);
        return root->val + max(left, right);
    }
};
