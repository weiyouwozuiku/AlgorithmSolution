class LRUCache {
    // lruç®—æ³•ä½¿ç”¨ä¸€ä¸ªunordered_mapå­˜å‚¨key,value.å†ä½¿ç”¨ä¸€ä¸ªåŒé“¾è¡¨æ ‡è®°ä½¿ç”¨çš„æ—¶é—´ã€‚æœ€è¿‘ä½¿ç”¨åˆ™åœ¨åŒé“¾è¡¨å¤´éƒ¨ï¼Œæœ€ä¸å¸¸ä½¿ç”¨çš„åˆ™åœ¨åŒé“¾è¡¨çš„å°¾éƒ¨
public:
    struct Node {
        int key, val;
        Node *left, *right;

        Node(int _val, int _key) : val(_val), key(_key), left(nullptr), right(nullptr) {}
    } *l, *r;

    unordered_map<int, Node *> hash;

    int cap;

    LRUCache(int capacity) {
        cap = capacity;
        l = new Node(-1, -1), r = new Node(-1, -1);
        l->right = r;
        r->left = l;
    }

    int get(int key) {
        if (hash.count(key)) {
            auto p = hash[key];
            remove(p);
            insert(p);
            return p->val;
        } else {
            return -1;
        }
    }

    void put(int key, int value) {
        if (hash.count(key)) {
            auto p = hash[key];
            p->val = value;
            remove(p);
            insert(p);
        } else {
            if (hash.size() == cap) {
                auto p = r->left;
                remove(p);
                hash.erase(p->key);
                delete p;
            }
            auto p = new Node(value, key);
            hash[key] = p;
            insert(p);
        }
    }

    void remove(const Node *node) {
        node->left->right = node->right;
        node->right->left = node->left;
    }

    void insert(Node *node) {
        node->right = l->right;
        node->left = l;
        l->right->left = node;
        l->right = node;
    }
};

