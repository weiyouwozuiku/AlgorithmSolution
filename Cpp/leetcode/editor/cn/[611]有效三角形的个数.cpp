class Solution {
public:
    int triangleNumber(vector<int> &nums) {
        int l = 0, r = nums.size() - 1, res = 0;
        sort(nums.begin(), nums.end());
        for (int k = 2; k <= r; ++k) {
            int i = 0, j = k - 1;
            while (i < j) {
                if (nums[i] + nums[j] > nums[k]) {
                    res += j - i;
                    j -= 1;
                } else {
                    i += 1;
                }
            }
        }
        return res;
    }
};
