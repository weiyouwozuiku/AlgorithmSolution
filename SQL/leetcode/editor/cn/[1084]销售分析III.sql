select
    a.product_id,
    a.product_name
from
    Product a
        left join (
        select
            product_id
        from
            Sales
        group by
            product_id
        having
                min(sale_date) >= '2019-01-01'
           and max(sale_date) <= '2019-03-31'
    ) b on a.product_id = b.product_id
where
    b.product_id is not null;
